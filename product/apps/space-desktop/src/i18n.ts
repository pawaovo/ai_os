export function normalizeAppLanguage(value: unknown, fallback: AppLanguage = DEFAULT_APP_LANGUAGE): AppLanguage {
  if (typeof value !== "string") return fallback;
  return value.toLowerCase().startsWith("zh") ? "zh-CN" : "en";
}

export function translateApp(
  language: AppLanguage,
  key: string,
  variables: Record<string, string | number> = {},
): string {
  const normalized = normalizeAppLanguage(language);
  const template = translations[normalized][key] ?? translations.en[key] ?? key;
  return template.replace(/\{\{(\w+)\}\}/g, (_match, variable) => String(variables[variable] ?? ""));
}

export function translatedValuesForKey(key: string): string[] {
  return Object.values(translations)
    .map((table) => table[key])
    .filter((value): value is string => Boolean(value));
}
